def _mean(vals):
    valid = [v for v in vals if v is not None]
    return round(sum(valid) / len(valid), 1) if valid else None


def _trend_arrow(vals):
    """Return trend direction from last 7 values."""
    recent = [v for v in vals[-7:] if v is not None]
    if len(recent) < 3:
        return ""
    first_half = _mean(recent[: len(recent) // 2])
    second_half = _mean(recent[len(recent) // 2 :])
    if first_half is None or second_half is None:
        return ""
    diff = second_half - first_half
    if diff > 2:
        return "&#8593;"  # up arrow
    if diff < -2:
        return "&#8595;"  # down arrow
    return "&#8594;"  # right arrow (stable)


def _svg_trend(values, width=460, height=80):
    """Generate a simple SVG trend line for the PDF."""
    pts = [(i, v) for i, v in enumerate(values) if v is not None]
    if len(pts) < 2:
        return '<div style="height:80px;color:#8B7F75;font-size:11px;">Insufficient data</div>'

    vals = [v for _, v in pts]
    vmin = min(vals) - 2
    vmax = max(vals) + 2
    vrange = vmax - vmin or 1
    avg = sum(vals) / len(vals)

    pad_l, pad_r, pad_t, pad_b = 4, 4, 8, 8
    cw = width - pad_l - pad_r
    ch = height - pad_t - pad_b

    def x(i):
        return pad_l + i * cw / (len(values) - 1)

    def y(v):
        return pad_t + (vmax - v) * ch / vrange

    base_y = y(avg)

    svg = f'<svg viewBox="0 0 {width} {height}" xmlns="http://www.w3.org/2000/svg" style="width:100%;height:{height}px;">'
    # Baseline
    svg += f'<line x1="{pad_l}" y1="{base_y:.1f}" x2="{width - pad_r}" y2="{base_y:.1f}" stroke="#E8DFD5" stroke-width="1" stroke-dasharray="3 3"/>'
    # avg label
    svg += f'<text x="{width - pad_r}" y="{base_y - 4:.1f}" text-anchor="end" fill="#8B7F75" font-family="JetBrains Mono,monospace" font-size="8">avg {round(avg)}</text>'

    # Line
    path_d = " ".join(
        f"{'M' if j == 0 else 'L'}{x(i):.1f},{y(v):.1f}" for j, (i, v) in enumerate(pts)
    )
    svg += f'<path d="{path_d}" stroke="#1A1613" stroke-width="1.2" fill="none" stroke-linecap="round" stroke-linejoin="round"/>'

    # Last dot
    last_i, last_v = pts[-1]
    svg += f'<circle cx="{x(last_i):.1f}" cy="{y(last_v):.1f}" r="3" fill="#6B1E1E"/>'

    svg += "</svg>"
    return svg


