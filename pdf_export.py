def _build_cycle_section(cycle_info: dict, data: dict, keys: list) -> str:
    """Build the cycle history HTML section."""
    if not cycle_info:
        return """<div class="placeholder">
          <div class="placeholder-label">No cycle data logged</div>
          Cycle tracking can be enabled in the Soma app.<br>
          Phase-aware insights appear after logging two or more cycles.
        </div>"""

    phase = cycle_info.get("phase", "unknown")
    day = cycle_info.get("day_of_cycle", "—")
    avg_len = cycle_info.get("average_length", 28)
    predicted = cycle_info.get("predicted_next_start", "—")

    # Phase distribution bar
    phases_def = [
        ("menstrual", "Menstrual", 5),
        ("follicular", "Follicular", 8),
        ("ovulatory", "Ovulatory", 3),
        ("luteal", "Luteal", max(avg_len - 20, 6)),
        ("late_luteal", "Late luteal", 4),
    ]
    total = sum(d for _, _, d in phases_def)
    bar_html = '<div class="phase-bar">'
    for pid, plabel, pdays in phases_def:
        pct = pdays / total * 100
        bar_html += f'<div class="phase-seg phase-{pid}" style="flex:{pct:.1f}">{plabel}</div>'
    bar_html += "</div>"

    legend_html = '<div class="phase-legend">'
    colors = {"menstrual": "#6B1E1E", "follicular": "#2D4A3A", "ovulatory": "#A0691E", "luteal": "#8B7F75", "late_luteal": "#6B5F55"}
    for pid, plabel, pdays in phases_def:
        legend_html += f'<div class="phase-legend-item"><div class="phase-dot" style="background:{colors[pid]}"></div>{plabel} ({pdays}d)</div>'
    legend_html += "</div>"

    pattern_obs = cycle_info.get("pattern_observation", "")
    pattern_html = ""
    if pattern_obs:
        pattern_html = f'<p style="font-size:9pt;color:#1A1613;margin-top:16px;line-height:1.6;">{pattern_obs}</p>'

    return f"""
    <table class="summary-table" style="max-width:340px;margin-bottom:20px;">
      <tbody>
        <tr><td>Current phase</td><td>{phase.replace('_',' ').title()}</td></tr>
        <tr><td>Cycle day</td><td>{day}</td></tr>
        <tr><td>Average length</td><td>{avg_len} days</td></tr>
        <tr><td>Predicted next</td><td>{_fmt_date(predicted) if predicted != '—' else '—'}</td></tr>
      </tbody>
    </table>
    {bar_html}
    {legend_html}
    {pattern_html}
    <p style="font-size:8pt;color:#8B7F75;margin-top:12px;">
      Symptom logging is not yet available. Future versions will display logged symptoms alongside phase data.
    </p>
    """


def _fmt_date(date_str: str) -> str:
    """Format date string to '7 May 2026'."""
    try:
        d = date.fromisoformat(date_str)
        months = ["Jan", "Feb", "Mar", "Apr", "May", "Jun", "Jul", "Aug", "Sep", "Oct", "Nov", "Dec"]
        return f"{d.day} {months[d.month - 1]} {d.year}"
    except (ValueError, TypeError):
        return str(date_str)


def _fmt_date_short(date_str: str) -> str:
    """Format date to '7/5'."""
    try:
        d = date.fromisoformat(date_str)
        return f"{d.day}/{d.month}"
    except (ValueError, TypeError):
        return str(date_str)


async def generate_pdf(data: dict, cycle_info: dict = None) -> bytes:
    """Generate the clinical record PDF. Returns raw PDF bytes."""
    from playwright.async_api import async_playwright

    html = _build_html(data, cycle_info)

    async with async_playwright() as p:
        browser = await p.chromium.launch()
        page = await browser.new_page()
        await page.set_content(html, wait_until="networkidle")
        pdf_bytes = await page.pdf(
            format="A4",
            margin={"top": "28mm", "right": "24mm", "bottom": "32mm", "left": "24mm"},
            print_background=True,
            display_header_footer=True,
            header_template='<span></span>',
            footer_template="""
                <div style="width:100%;font-family:sans-serif;font-size:8px;color:#8B7F75;display:flex;justify-content:space-between;padding:0 24mm;">
                    <span>Generated by Soma. For clinical use.</span>
                    <span><span class="pageNumber"></span> / <span class="totalPages"></span></span>
                </div>
            """,
        )
        await browser.close()

    return pdf_bytes
